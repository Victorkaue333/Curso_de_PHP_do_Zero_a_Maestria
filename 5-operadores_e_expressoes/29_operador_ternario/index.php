<?php
// operador ternario
// funciona como um if simplificado
// A interrogação é para uma (condição) ? e o : para o (se for verdadeiro) : (se for falso);

// Exemplo 1 - verdadeiro
echo 30 > 10? "Verdadeiro" : "Falso";
echo "<br>";


// Exemplo 2 - falso
echo 20 > 30 ? "Verdadeiro" : "Falso";
echo "<br>";

// Exemplo 3 - para saber se uma pessoa é maior de idade
$idade = 20;
echo $idade >= 18 ? "Maior de idade" : "Menor de idade";
echo "<br>";


?>