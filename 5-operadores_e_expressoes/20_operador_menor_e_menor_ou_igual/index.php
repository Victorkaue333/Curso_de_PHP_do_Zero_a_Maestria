<?php
// Operador menor (<) e menor ou igual (<=)
$a = 2;
$b = 3;

// Verifica se $a é menor que $b
if($a < $b) {
    echo "$a é menor que $b <br>";
} else {
    echo "$a é maior que $b <br>";
}


// Verifica se $a é menor ou igual a $b
if($a <= $b) {
    echo "$a é menor ou igual a $b <br>";
} else {
    echo "$a é maior que $b <br>";
}
?>